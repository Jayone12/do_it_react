const string1 = '안녕하세요.';
const string2 = '반값습니다.';

console.log(`${string1} ${string2}`); // 안녕하세요. 반갑습니다.

const product = {
  name: '도서',
  price: '4200원',
};

console.log(`${product.name}의 가격은 ${product.price}입니다.`); // 도서의 가격은 4200원 입니다.

const boolValue = false;
console.log(`${boolValue ? '참' : '거짓'}`); // 거짓
